/**
 * 翻译地区名称
 */
export function translateRegion(name, type = 'country') {
    if (!name) return '未知';
    
    const map = type === 'country' ? COUNTRY_MAP 
        : type === 'province' ? PROVINCE_MAP 
        : CITY_MAP;
    
    return map[name] || name;
}

/**
 * 翻译地理信息对象
 */
export function translateGeo(geo) {
    if (!geo) return null;
    
    const translatedCity = translateRegion(geo.city, 'city');
    
    return {
        ip: geo.ip,
        country_name: translateRegion(geo.country_name, 'country'),
        region: translateRegion(geo.region, 'province'),
        city: translatedCity,
        district: geo.district ? translateRegion(geo.district, 'city') : translatedCity
    };
}